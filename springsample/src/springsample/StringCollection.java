package springsample;

import java.util.*;

public class StringCollection {
	private List strList;
	private Set strSet;
	private Map strMap;
	private Properties strProps;
	
	public List getStrList() {
		return strList;
	}
	public void setStrList(List strList) {
		this.strList = strList;
	}
	public Set getStrSet() {
		return strSet;
	}
	public void setStrSet(Set strSet) {
		this.strSet = strSet;
	}
	public Map getStrMap() {
		return strMap;
	}
	public void setStrMap(Map strMap) {
		this.strMap = strMap;
	}
	public Properties getStrProps() {
		return strProps;
	}
	public void setStrProps(Properties strProps) {
		this.strProps = strProps;
	}
	
	
}
